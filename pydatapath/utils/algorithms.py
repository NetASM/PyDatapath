import struct
import array

# CRC16 ####

if struct.pack("H", 1) == "\x00\x01":  # big endian
    def crc16(packet):
        if len(packet) % 2 == 1:
            packet += "\0"
        s = sum(array.array("H", packet))
        s = (s >> 16) + (s & 0xffff)
        s += s >> 16
        s = ~s
        return s & 0xffff
else:
    def crc16(packet):
        if len(packet) % 2 == 1:
            packet += "\0"
        s = sum(array.array("H", packet))
        s = (s >> 16) + (s & 0xffff)
        s += s >> 16
        s = ~s
        return (((s >> 8) & 0xff) | s << 8) & 0xffff
